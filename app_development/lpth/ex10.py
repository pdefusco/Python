#working with tabs to modify character text:

#\ encodes difficult to type characters
#\n lead to a new line
#\' allows to put a quote within text without ending the text string
#\t creates a tab
#\\ allows to print an actual tab

tabbed_string = "\t this is a tabbed string"
string2 = "\n new line string"
string3 = "this string uses a quote\'"
string4 = "more tabs \t in this string"
string5 = "now printing a tab \\"

print(tabbed_string)
print(string2)
print(string3)
print(string4)
print(string5)
